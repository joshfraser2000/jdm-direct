export function buildVehicleList(): Vehicle[] {
  return RAW_VEHICLES.map((v) => {
    const eligible = isVehicleEligible(v.year)
    return {
      ...v,
      totalLandedCost: v.price + v.shippingEstimate + Math.round(v.price * 0.025),
      isEligible: eligible,
      eligibilityDate: getEligibilityDate(v.year),
    }
  })
}

